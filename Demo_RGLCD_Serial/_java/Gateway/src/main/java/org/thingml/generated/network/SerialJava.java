package org.thingml.generated.network;

import org.thingml.java.*;
import org.thingml.java.ext.*;

import org.thingml.generated.messages.*;

import jssc.SerialPort;
import jssc.SerialPortEvent;
import jssc.SerialPortEventListener;
import jssc.SerialPortException;

import java.util.Arrays;

public class SerialJava extends Component {

    private final String port;
    private final int baudrate;

    private final SerialPort serialPort;
    private final byte START_BYTE = 0x12;
    private final byte STOP_BYTE = 0x13;
    private final byte ESCAPE_BYTE = 0x7D;

    /*$MESSAGE TYPES$*/
protected final Button_long_pressMessageType button_long_pressType = new Button_long_pressMessageType((short)2);

protected final Button_pressMessageType button_pressType = new Button_pressMessageType((short)0);

protected final Button_releaseMessageType button_releaseType = new Button_releaseMessageType((short)1);

protected final Set_bgcolorMessageType set_bgcolorType = new Set_bgcolorMessageType((short)3);


    /*$PORTS$*/
private Port myPort_port;
public Port getMyPort_port(){return myPort_port;}


    public SerialJava(final String port, final int baudrate) {
        this.port = port;
        this.baudrate = baudrate;
        serialPort = new SerialPort(port);
        try {
            serialPort.openPort();
            serialPort.setParams(baudrate, 8, 1, 0);
            serialPort.setFlowControlMode(SerialPort.FLOWCONTROL_RTSCTS_IN | SerialPort.FLOWCONTROL_RTSCTS_OUT);
            serialPort.addEventListener(new SerialPortReader());
        }
        catch (SerialPortException ex) {
            System.out.println(ex);
        }
    }

    private void send(final byte[] payload) {
        try {
            serialPort.writeByte(START_BYTE);
            for (byte b : payload) {
                if (b == START_BYTE || b == STOP_BYTE || b == ESCAPE_BYTE) {
                    serialPort.writeByte(ESCAPE_BYTE);
                }
                serialPort.writeByte(b);
            }
            serialPort.writeByte(STOP_BYTE);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void parse(final byte[] payload) {
        Event result = null;
result = set_bgcolorType.instantiate(payload, "default");
if (result != null) {
myPort_port.send(result);
result = null;
}
result = button_releaseType.instantiate(payload, "default");
if (result != null) {
myPort_port.send(result);
result = null;
}
result = button_pressType.instantiate(payload, "default");
if (result != null) {
myPort_port.send(result);
result = null;
}
result = button_long_pressType.instantiate(payload, "default");
if (result != null) {
myPort_port.send(result);
result = null;
}

    }

    @Override
    public void run() {
        while(active) {
            try {
                final Event e = queue.take();//should block if queue is empty, waiting for a message
                send(e.toBytes("default"));
            } catch (InterruptedException e) {
                //e.printStackTrace();
            }
        }
    }

    @Override
    public Component buildBehavior(String id, Component root) {
        /*$INIT PORTS$*/
myPort_port = new Port(PortType.REQUIRED, "myPort", this);

        return this;
    }

    class SerialPortReader implements SerialPortEventListener {

        public static final int RCV_WAIT = 0;
        public static final int RCV_MSG = 1;
        public static final int RCV_ESC = 2;
        private byte[] buffer = new byte[256];
        protected int buffer_idx = 0;
        protected int state = RCV_WAIT;

        public void serialEvent(SerialPortEvent event) {
            try {
                if (event.isRXCHAR() && event.getEventValue() > 0) {
                    byte[] received = serialPort.readBytes();
                    for (byte data : received) {
                        if (state == RCV_WAIT) { // it should be a start byte or we just ignore it
                            if (data == START_BYTE) {
                                state = RCV_MSG;
                                buffer_idx = 0;
                            }
                        } else if (state == RCV_MSG) {
                            if (data == ESCAPE_BYTE) {
                                state = RCV_ESC;
                            } else if (data == STOP_BYTE) {
                                parse(Arrays.copyOf(buffer, buffer_idx));
                                state = RCV_WAIT;
                            } else if (data == START_BYTE) {
                                // Should not happen but we reset just in case
                                state = RCV_MSG;
                                buffer_idx = 0;
                            } else { // it is just a byte to store
                                buffer[buffer_idx] = (byte) data;
                                buffer_idx++;
                            }
                        } else if (state == RCV_ESC) {
                            // Store the byte without looking at it
                            buffer[buffer_idx] = (byte) data;
                            buffer_idx++;
                            state = RCV_MSG;
                        }
                    }
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }
}
